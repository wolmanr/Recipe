Ingredients and Measurements:
4 large ripe tomatoes, chopped
1 medium onion, diced
2 cloves garlic, minced
2 tbsp olive oil
2 cups vegetable broth
1 tsp sugar
1 tsp salt (or to taste)
1/2 tsp black pepper
1/4 tsp dried basil (optional)
1/4 tsp dried oregano (optional)
1/4 cup heavy cream (optional, for creaminess)

Recipe Steps:
1. Prepare the ingredients: Wash and chop the tomatoes into chunks. Dice the onion and mince the garlic.

2. Sauté aromatics: Heat the olive oil in a large pot over medium heat. Add the diced onion and cook for about 5 minutes until softened. 
Add the minced garlic and cook for an additional 1 minute, stirring constantly to avoid burning.

3. Cook tomatoes: Add the chopped tomatoes to the pot. Stir occasionally and cook for about 10 minutes until the tomatoes break down and become soft.

4. Simmer the soup: Add the vegetable broth, sugar, salt, pepper, and any optional dried herbs like basil or oregano. Bring the mixture to a boil, 
then reduce the heat to low. Let it simmer for 15–20 minutes to meld the flavors together.

5. Blend the soup: Use an immersion blender directly in the pot or transfer the soup in batches to a regular blender. Puree the soup until smooth.

6. Add cream (optional): Stir in the heavy cream for extra richness and creaminess. Adjust the seasoning with salt and pepper as needed.

7. Serve: Ladle the soup into bowls and serve warm. Garnish with fresh herbs or croutons if desired.


